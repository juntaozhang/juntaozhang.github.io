    int[][] directions = new int[][]{
            {-1, 0},
            {1, 0},
            {0, 1},
            {0, -1}
    };

    public boolean hasZeroEdge(int[][] grid, int sr, int sc) {
        int x, y;
        int m = grid.length, n = grid[0].length;
        for (int[] d : directions) {
            x = d[0] + sr;
            y = d[1] + sc;
            if (x >= 0 && x < m && y >= 0 && y < n) {
                if (grid[x][y] == 0) {
                    return true;
                }
            }
        }
        return false;
    }

    public int shortestBridge(int[][] grid) {
        int m = grid.length, n = grid[0].length;
        int[][] vist = getIsland(grid);
        if (vist == null) {
            return -1;
        }

        Queue<int[]> q = new LinkedList<>();
        int x, y, p;

        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (vist[i][j] == 1) {
                    // 只添加边缘
                    if (hasZeroEdge(grid, i, j)) {
                        q.offer(new int[]{i, j, 0});
                    }
                }
            }
        }

        while (!q.isEmpty()) {
            int[] t = q.poll();
            for (int[] d : directions) {
                x = d[0] + t[0];
                y = d[1] + t[1];
                p = t[2] + 1;
                if (x >= 0 && x < m && y >= 0 && y < n && vist[x][y] == 0) {
                    if (grid[x][y] == 0) {
                        vist[x][y] = 2;
                        q.offer(new int[]{x, y, p});
                    } else {
                        return p - 1;
                    }
                }
            }
        }
        return -1;
    }

    public int[][] getIsland(int[][] grid) {
        int m = grid.length, n = grid[0].length;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (grid[i][j] == 1) {
                    return bfs(grid, i, j);
                }
            }
        }
        return null;
    }

    public int[][] bfs(int[][] grid, int sr, int sc) {
        int m = grid.length, n = grid[0].length;
        Queue<int[]> q = new LinkedList<>();
        int[][] vist = new int[m][n];
        q.offer(new int[]{sr, sc});
        vist[sr][sc] = 1;
        int x, y;
        while (!q.isEmpty()) {
            int[] t = q.poll();
            for (int[] d : directions) {
                x = d[0] + t[0];
                y = d[1] + t[1];
                if (x >= 0 && x < m && y >= 0 && y < n && grid[x][y] == 1 && vist[x][y] == 0) {
                    q.offer(new int[]{x, y});
                    vist[x][y] = 1;
                }
            }
        }
        return vist;
    }

